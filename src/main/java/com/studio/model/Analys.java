package com.studio.model;

public class Analys {
	int nam, thang, tong;

	public int getNam() {
		return nam;
	}

	public void setNam(int nam) {
		this.nam = nam;
	}

	public int getThang() {
		return thang;
	}

	public void setThang(int thang) {
		this.thang = thang;
	}

	public int getTong() {
		return tong;
	}

	public void setTong(int tong) {
		this.tong = tong;
	}

	public Analys(int nam, int thang, int tong) {
		super();
		this.nam = nam;
		this.thang = thang;
		this.tong = tong;
	}

	public Analys() {
		super();
	}
	
	

}
